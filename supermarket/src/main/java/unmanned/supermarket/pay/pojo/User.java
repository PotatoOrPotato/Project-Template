package unmanned.supermarket.pay.pojo;

public class User {

    private String gid;
    private int bb;
    private String name;

    public String getGid() {
        return gid;
    }
    public void setGid(String gid) {
        this.gid = gid;
    }
    public int getBb() {
        return bb;
    }
    public void setBb(int bb) {
        this.bb = bb;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
